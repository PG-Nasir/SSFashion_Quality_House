package pg.model;

public class ModuleInfo {

	String name;
	int ware;
	int user;
	int active;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getWare() {
		return ware;
	}
	public void setWare(int ware) {
		this.ware = ware;
	}

	public int getUser() {
		return user;
	}
	public void setUser(int user) {
		this.user = user;
	}
	public int getActive() {
		return active;
	}
	public void setActive(int active) {
		this.active = active;
	}
	
	
}
