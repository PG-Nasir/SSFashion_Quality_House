package pg.model;

public class UserAccessModule {

	int id;
	int user;
	String combineModuleList;
	
	
	
	public int getUser() {
		return user;
	}
	public void setUser(int user) {
		this.user = user;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCombineModuleList() {
		return combineModuleList;
	}
	public void setCombineModuleList(String combineModuleList) {
		this.combineModuleList = combineModuleList;
	}
	
	
}
